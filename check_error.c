#include "push_swap.h"

void	ft_putstr(char *str)
{
	int	i;

	i = 0;
	while (str[i])
	{
		write(1, &str[i], 1);
		i++;
	}
}

void	check_error(int i, char *str)
{
	if (i < 0)
	{
		ft_putstr(str);
		exit(1);
	}
}
